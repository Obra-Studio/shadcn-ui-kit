	<h1>Johan Ronsse</h1>

	<h2>About</h2>

	<p>Johan Ronsse is a designer and developer based in Belgium. He is the creator and maintainer of the Obra shadcn/ui Figma kit, a comprehensive design library that recreates all shadcn/ui components for Figma.</p>

	<p>With years of experience in design systems and component libraries, Johan and his design team at Obra created this kit to bridge the gap between design and development, making it easier for teams to prototype and build with shadcn/ui.</p>

