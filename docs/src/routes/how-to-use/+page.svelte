	<h1>How to use this kit</h1>

	<p>This kit is meant to be customised further. It's the start of your own system.</p>

	<p>Just like shadcn/ui, this is not meant to be a final component library. It's how you start building your component library.</p>

	<p>To make the kit your own, we recommend to:</p>

	<ol>
		<li>Remove all blue frames. They are meant as documentation.</li>
		<li>Customise the thumbnail to your liking. Give it the name of your design system.</li>
		<li>Customize the theme to your brand</li>
		<li>Now publish the library to your Figma account (this requires at least a professional account).</li>
	</ol>

	<p>Then, consume the library from another file. You can use the bookmark icon in the assets panel to find the controls to do so.</p>

